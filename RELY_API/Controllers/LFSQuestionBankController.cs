        // POST: api/LFSQuestionBank
        //Method to Post Data from app to db
        [ResponseType(typeof(LFSQuestionBank))]
        public async Task<IHttpActionResult> PostLFSQuestionBanks(LFSQuestionBank LFSQuestionBank, string UserName, string WorkFlow)
        {
            if (!ModelState.IsValid)
            {
                throw new HttpResponseException(Request.CreateErrorResponse(HttpStatusCode.BadRequest, string.Format(Globals.BadRequestErrorMessage, "CREATE","QuestionBank")));
            }

            try
            {
                if (db.LFSQuestionBanks.Where(p => p.Id == LFSQuestionBank.Id).Where(p => p.CompanyCode == LFSQuestionBank.CompanyCode).Count() > 0)
                {
                    db.Entry(LFSQuestionBank).State = EntityState.Modified;
                    await db.SaveChangesAsync();
                }
                else
                {
                    LFSQuestionBank.Id = 0;//To override the Id generated by grid
                    db.LFSQuestionBanks.Add(LFSQuestionBank);
                    await db.SaveChangesAsync();
                }
            }
            catch (DbEntityValidationException dbex)
            {

                var errorMessage = Globals.GetEntityValidationErrorMessage(dbex);
                throw new HttpResponseException(Request.CreateErrorResponse((HttpStatusCode)Globals.ExceptionType.Type2, errorMessage));//type 2 error
            }
            catch (Exception ex)
            {

                if (ex.GetBaseException().GetType() == typeof(SqlException))//check for exception type
                {
                    //Throw this as HttpResponse Exception to let user know about the mistakes they made, correct them and retry.
                    throw new HttpResponseException(Request.CreateErrorResponse((HttpStatusCode)Globals.ExceptionType.Type2, GetCustomizedErrorMessage(ex)));//type 2 error
                }
                else
                {
                    throw ex;//This exception will be handled in FilterConfig's CustomHandler
                }
            }
            return CreatedAtRoute("DefaultApi", new { id = LFSQuestionBank.Id }, LFSQuestionBank);
        }

        [HttpPost]
        [ResponseType(typeof(LFSQuestionBank))]
        public async Task<IHttpActionResult> PostLFSQuestionBank(List<LFSQuestionBank> LFSQuestionBank, string UserName, string WorkFlow)
        {
            if (!ModelState.IsValid)
            {
                throw new HttpResponseException(Request.CreateErrorResponse(HttpStatusCode.BadRequest, string.Format(Globals.BadRequestErrorMessage, "CREATE", "LFSQuestionBank")));
            }
            using (var transaction = db.Database.BeginTransaction())
            {
                try
                {
                    int LoggedInUserId = 0;
                    foreach (var model in LFSQuestionBank)
                    {
                        if (model.Id == 0)//add only when new record is there to insert.
                            db.LFSQuestionBanks.Add(model);
                        LoggedInUserId = model.UpdatedById;
                        await db.SaveChangesAsync();
                    }

                }
                catch (DbEntityValidationException dbex)
                {
                    transaction.Rollback();
                    var errorMessage = Globals.GetEntityValidationErrorMessage(dbex);
                    throw new HttpResponseException(Request.CreateErrorResponse((HttpStatusCode)Globals.ExceptionType.Type2, errorMessage));//type 2 error
                }
                catch (Exception ex)
                {
                    transaction.Rollback();
                    if (ex.GetBaseException().GetType() == typeof(SqlException))//check for exception type
                    {
                        //Throw this as HttpResponse Exception to let user know about the mistakes they made, correct them and retry.
                        throw new HttpResponseException(Request.CreateErrorResponse((HttpStatusCode)Globals.ExceptionType.Type2, GetCustomizedErrorMessage(ex)));//type 2 error
                    }
                    else
                    {
                        throw ex;//This exception will be handled in FilterConfig's CustomHandler
                    }
                }

                transaction.Commit();
            }

            return Ok();
        }

