        {
            LFSQuestionBank LFSQuestionBank = await db.LFSQuestionBanks.FindAsync(id);
            if (LFSQuestionBank == null)
            {
                throw new HttpResponseException(Request.CreateErrorResponse(HttpStatusCode.NotFound, string.Format(Globals.NotFoundErrorMessage, "Question Bank")));
            }

            try
            {
                db.LFSQuestionBanks.Remove(LFSQuestionBank);
                await db.SaveChangesAsync();

            }
            catch (Exception ex)
            {
                if (ex.GetBaseException().GetType() == typeof(SqlException))//check for exception type
                {
                    //Throw this as HttpResponse Exception to let user know about the mistakes they made, correct them and retry. 
                    throw new HttpResponseException(Request.CreateErrorResponse((HttpStatusCode)Globals.ExceptionType.Type2, GetCustomizedErrorMessage(ex)));//type 2 error
                }
                else
                {
                    throw ex;
                }
            }
            return Ok(LFSQuestionBank);
        }
        private bool LFSQuestionBankExists(string CompanyCode)
        {
            return db.LFSQuestionBanks.Count(e => e.CompanyCode == CompanyCode) > 0;
        }

